package co.salutary.mobisaude.db.entidade;

public class UF {

	private int idUf;
	private String sigla;
	private String nome;

	public int getIdUf() {
		return idUf;
	}
	public void setIdUf(int idUf) {
		this.idUf = idUf;
	}
	public String getSigla() {
		return sigla;
	}
	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}